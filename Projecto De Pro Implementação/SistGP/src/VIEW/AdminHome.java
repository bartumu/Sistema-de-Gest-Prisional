    private void ValidarData(String data) {
        DateTimeFormatter formato = DateTimeFormatter.ofPattern("uuuu-MM-dd");
        try {
            formato.parse(data);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Formato data errada, tem que ser Ano-Mes-Dia");

        }
    }

